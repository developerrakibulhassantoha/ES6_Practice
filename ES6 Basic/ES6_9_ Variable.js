//  Variable var can be  Re-assigned
/* Variable
   Var 
   let
   const*/

var Name="Rakibul Hassan";
Name="Toha";
console.log(Name)

//  Variable let can be Re-assigned
let Name="Rakibul Hassan";
Name="Karim";
console.log(Name)

//  Variable cannot be const to Re-assigned 
const Name="Rakibul Hassan";
Name="Babo";
console.log(Name)


//  Variable var can be  Re-declared
var Name="Rakibul Hassan";
var Name="Toha Master";
console.log(Name)


//  Variable let cannot be  Re-declared
let Name="Rakibul Hassan";
let Name="Master";
console.log(Name)


//  Variable const cannot be  Re-declared
const Name="Rakibul Hassan";
const Name="Master";
console.log(Name)

